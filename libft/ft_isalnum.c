int	ft_sep_ex(char line)
{
	if (line != 36 && line != 34 && line != 39)
		return (0);
	return (1);
}

int	ft_isalnum(int c)
{
	if ((c >= 48 && c <= 57) || (c >= 65 && c <= 90)
		|| (c >= 97 && c <= 122))
		return (1);
	return (0);
}
